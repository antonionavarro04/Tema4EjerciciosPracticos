package ejercicio4;

public class Operaciones {
    // ^ Definimos las variables num1 y num2
    double num1;
    double num2;
    
    // ^ Definimos el método suma
    double suma() {
        return num1 + num2;
    }

    // ^ Definimos el método resta
    double resta() {
        return num1 - num2;
    }

    // ^ Definimos el método multiplicación
    double multiplicacion() {
        return num1 * num2;
    }
    
    // ^ Definimos el método división
    double division() {
        return num1 / num2;
    }
}
