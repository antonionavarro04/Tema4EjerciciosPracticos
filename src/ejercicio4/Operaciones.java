package ejercicio4;

public class Operaciones {
    // ^ Definimos las variables num1 y num2
    public double num1;
    public double num2;
    
    // ^ Definimos el método suma
    public double suma() {
        return num1 + num2;
    }

    // ^ Definimos el método resta
    public double resta() {
        return num1 - num2;
    }

    // ^ Definimos el método multiplicación
    public double multiplicacion() {
        return num1 * num2;
    }

    // ^ Definimos el método división
    public double division() {
        return num1 / num2;
    }
}
